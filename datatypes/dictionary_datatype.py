d2 = {1:'chandan', 2:'darshan', 3:'chitra'}

#find the element --> Normal way
print(d2[1]) #Result --> chandan #Here 1 is Key
print(d2[2]) #Result --> darshan #Here 2 is Key


#find the element --> GET Method
print(d2.get(1)) #Result --> chandan #Here 1 is Key
print(d2.get(2)) #Result --> darshan #Here 2 is Key
