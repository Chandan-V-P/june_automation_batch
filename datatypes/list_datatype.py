#Addition in LIST
ls1 = [1,2,3]
ls2 = [4,5,6]

addition_list = ls1+ls2
print("addition of list is",addition_list) #[1, 2, 3, 4, 5, 6]

#Multiplication in LIST
mul_ls1 = ls1*3
print("multiplication of list",mul_ls1) #[1, 2, 3, 1, 2, 3, 1, 2, 3]
mul_ls2 = ls2*5
print("multiplication of list",mul_ls2) #[4, 5, 6, 4, 5, 6, 4, 5, 6, 4, 5, 6, 4, 5, 6]
